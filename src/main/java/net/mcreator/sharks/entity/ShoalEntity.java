	public ShoalEntity(EntityType<ShoalEntity> type, Level world) {
		super(type, world);
		xpReward = 0;
		setNoAi(false);
		setMaxUpStep(0f);
		this.setPathfindingMalus(BlockPathTypes.WATER, 0);
		this.moveControl = new MoveControl(this) {
			@Override
			public void tick() {
				if (ShoalEntity.this.isInWater())
					ShoalEntity.this.setDeltaMovement(ShoalEntity.this.getDeltaMovement().add(0, 0.005, 0));
				if (this.operation == MoveControl.Operation.MOVE_TO && !ShoalEntity.this.getNavigation().isDone()) {
					double dx = this.wantedX - ShoalEntity.this.getX();
					double dy = this.wantedY - ShoalEntity.this.getY();
					double dz = this.wantedZ - ShoalEntity.this.getZ();
					float f = (float) (Mth.atan2(dz, dx) * (double) (180 / Math.PI)) - 90;
					float f1 = (float) (this.speedModifier * ShoalEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
					ShoalEntity.this.setYRot(this.rotlerp(ShoalEntity.this.getYRot(), f, 10));
					ShoalEntity.this.yBodyRot = ShoalEntity.this.getYRot();
					ShoalEntity.this.yHeadRot = ShoalEntity.this.getYRot();
					if (ShoalEntity.this.isInWater()) {
						ShoalEntity.this.setSpeed((float) ShoalEntity.this.getAttribute(Attributes.MOVEMENT_SPEED).getValue());
						float f2 = -(float) (Mth.atan2(dy, (float) Math.sqrt(dx * dx + dz * dz)) * (180 / Math.PI));
						f2 = Mth.clamp(Mth.wrapDegrees(f2), -85, 85);
						ShoalEntity.this.setXRot(this.rotlerp(ShoalEntity.this.getXRot(), f2, 5));
						float f3 = Mth.cos(ShoalEntity.this.getXRot() * (float) (Math.PI / 180.0));
						ShoalEntity.this.setZza(f3 * f1);
						ShoalEntity.this.setYya((float) (f1 * dy));
					} else {
						ShoalEntity.this.setSpeed(f1 * 0.05F);
					}
				} else {
					ShoalEntity.this.setSpeed(0);
					ShoalEntity.this.setYya(0);
					ShoalEntity.this.setZza(0);
				}
			}
		};
	}

